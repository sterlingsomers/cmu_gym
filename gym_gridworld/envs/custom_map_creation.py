def scale_image(image, scale):
    # Repeats the pattern in order to scale it
    return image.repeat(scale, axis=0).repeat(scale, axis=1)

def encode_map(drawn_map, x, y, scale):

    filename = '{}-{}'.format(x, y)
    drawn_map = np.array(drawn_map)
    map = CNP.create_custom_map(drawn_map) # This creates an 'img' structure in the map dictionary
    print("saving map.")
    pickle_in = open(path + 'maps/' + filename + '.mp', 'wb')
    pickle.dump(map, pickle_in)


    print("saving map image")
    image = scale_image(map['img'],scale)
    imageio.imwrite(path + 'maps/' + filename + '.png', image)



